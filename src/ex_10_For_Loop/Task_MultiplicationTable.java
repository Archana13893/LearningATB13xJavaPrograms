package ex_10_For_Loop;

public class Task_MultiplicationTable {
    public static void main(String[] args) {
        //Write a program to print the multiplication table of 5.
        int m =5;
        for(int i=1;i<=10;i++){
            System.out.println( m + "x" + i +" = " + (m*i));
        }
    }
}
