package ex_04_Operators;

public class Lab040_Relational_Operators {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        boolean a= 10>20;
        System.out.println(a);

        int x= 20;
        int y= 40;
        boolean result = x != y;
        System.out.println(result);


    }
}
